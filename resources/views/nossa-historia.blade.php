    <h1 class="text-center" >Nossa História</h1>

    <p>O Colégio Estadual de Monteiro, que hoje é a Escola Cidadã Integral Tecnica José Leite de Souza, foi construída no ano de 1972 em um terreno doado pelo então Deputado Estadual Euvaldo da Silva Brito, que veio da iniciativa do Prefeito Jorge Rafael de Menezes e teve como diretor e fundador o Dr. Antenor Campos. Atualmente o Colégio Estadual de Monteiro José leite de Souza, tem como responsável por sua gestão a Diretora Vera Paes.</p>

    <p>Optamos também por salientar a importância da Escola Cidadã Integral Técnica José Leite de Souza e o valor histórico-cultural que ela construiu e que ainda representa na vida dos cidadãos da comunidade de Monteiro, já que, dentro deste contexto, ela é, sem dúvida, um forte elemento da identidade local. Enquanto escola, buscamos criar um clima escolar, priorizando a tolerância, o respeito no cotidiano escolar, a prática da cidadania, além da alta expectativa na aprendizagem dos alunos, pois acreditamos que todos podem aprender, assim como defendemos que todos são iguais em direitos, apesar das diferenças individuais, por isso necessitam de tratamento pedagógico específico, bem planejado e acompanhado.</p>

    <h2>E por que escolher a nossa escola?</h2>

    <p>Por acreditar no potencial humano desta Comunidade Escolar, cremos que o resultado de nossas expectativas, para os anos letivos que viram, podem e devem ser acompanhados por avaliações processuais monitoradas, apresentando, portanto, resultados, notadamente, positivos.</p>

    <p>O desafio maior desta comunidade escolar é contribuir para um rumo novo no Ensino Médio à luz do Programa da Escola Cidadã Integral, proporcionando a todos condições de progredir e construir um futuro melhor, moderno e ético, desenvolvendo as habilidades e competências dos indivíduos que atendam aos três eixos deste modelo: Formação Acadêmica de Excelência, Formação para a Vida e Formação de Competências para o século XXI.</p>

    <h2>Como é dentro da escola?</h2>

    <p> A Escola Cidadã Integral Técnica José Leite de Souza dispõe de um Regimento Interno para atender as peculiaridades de sua estrutura, organização e funcionamento fundamentado na LDB, na Legislação de Ensino, nas Orientações e Diretrizes Educacionais do Programa de Escola Cidadã Integral e da Educação Profissional para o trabalho (EPT), implementadas pela Secretaria de Estado da Educação (SEE/PB), através da Gerência Executiva do Ensino Médio e Educação Profissional (GEEMEP/SEE/PB) que orienta e fiscaliza pedagogicamente e administrativamente as ações executadas nesta Unidade de Ensino, além do respaldo executado pelo Conselho Escolar atuante e Participativo.</p>

